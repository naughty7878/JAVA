package com.test.base01.ari;

/**
 * 运算符之一：+ - * / % ++ --
 * 运算符值二：= += -= *= /= %=
 */
public class AriTest {
    public static void main(String[] args) {

        boolean p = true;
        boolean q = false;
        System.out.println("p & q = " + (p & q));
        System.out.println("p | q = " + (p | q));
        System.out.println("!p = " + (!p));
    }
}
